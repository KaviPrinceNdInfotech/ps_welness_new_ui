class Supplier {
  final String name;
  final String email;
  final String address;
  final String mobile;
  final String pin;
  final String returnPolicy;
  final String office;

  const Supplier({
    required this.name,
    required this.email,
    required this.address,
    required this.mobile,
    required this.pin,
    required this.returnPolicy,
    required this.office,
  });
}
